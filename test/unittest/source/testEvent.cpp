#include "testEvent.h"


namespace test
{
////////////////////////////////////////////////////////////////////////////////
Event::Event(int id)
  : m_id(id)
  , mValue(0)
{
}
////////////////////////////////////////////////////////////////////////////////
Event::Event(int id, int value)
  : m_id(id)
  , mValue(value)
{
}
////////////////////////////////////////////////////////////////////////////////
int Event::get_id() const
{
    return m_id;
}
////////////////////////////////////////////////////////////////////////////////
int Event::getValue() const
{
    return mValue;
}
////////////////////////////////////////////////////////////////////////////////
bool operator==(Event const & lhs, Event const & rhs)
{
    return (
        (lhs.get_id() == rhs.get_id()) &&
        (lhs.getValue() == rhs.getValue()));
}
////////////////////////////////////////////////////////////////////////////////
bool operator!=(Event const & lhs, Event const & rhs)
{
    return !(lhs == rhs);
}
////////////////////////////////////////////////////////////////////////////////
}
