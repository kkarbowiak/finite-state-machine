#include "testEvent.h"


namespace test
{
////////////////////////////////////////////////////////////////////////////////
Event::Event(int id)
  : m_id(id)
  , m_value(0)
{
}
////////////////////////////////////////////////////////////////////////////////
Event::Event(int id, int value)
  : m_id(id)
  , m_value(value)
{
}
////////////////////////////////////////////////////////////////////////////////
int Event::get_id() const
{
    return m_id;
}
////////////////////////////////////////////////////////////////////////////////
int Event::get_value() const
{
    return m_value;
}
////////////////////////////////////////////////////////////////////////////////
bool operator==(Event const & lhs, Event const & rhs)
{
    return (
        (lhs.get_id() == rhs.get_id()) &&
        (lhs.get_value() == rhs.get_value()));
}
////////////////////////////////////////////////////////////////////////////////
bool operator!=(Event const & lhs, Event const & rhs)
{
    return !(lhs == rhs);
}
////////////////////////////////////////////////////////////////////////////////
}
