#include "testEvent.h"


namespace test
{
////////////////////////////////////////////////////////////////////////////////
Event::Event(int id)
  : mID(id)
  , mValue(0)
{
}
////////////////////////////////////////////////////////////////////////////////
Event::Event(int id, int value)
  : mID(id)
  , mValue(value)
{
}
////////////////////////////////////////////////////////////////////////////////
int Event::getID() const
{
    return mID;
}
////////////////////////////////////////////////////////////////////////////////
int Event::getValue() const
{
    return mValue;
}
////////////////////////////////////////////////////////////////////////////////
bool operator==(Event const & lhs, Event const & rhs)
{
    return (
        (lhs.getID() == rhs.getID()) &&
        (lhs.getValue() == rhs.getValue()));
}
////////////////////////////////////////////////////////////////////////////////
bool operator!=(Event const & lhs, Event const & rhs)
{
    return !(lhs == rhs);
}
////////////////////////////////////////////////////////////////////////////////
}
